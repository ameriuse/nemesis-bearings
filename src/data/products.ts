export function getProductBySlug(slug: string): Product | undefined {
  return sampleProducts.find(p => p.slug === slug);
}

export function getProductsByCategory(category: string): Product[] {
  return sampleProducts.filter(p => p.category === category);
}

export function getCategoryBySlug(slug: string): CategoryInfo | undefined {
  return categories.find(c => c.slug === slug);
}

export function searchProducts(query: string): Product[] {
  const q = query.toLowerCase().trim();
  if (!q) return sampleProducts;
  return sampleProducts.filter(p =>
    p.partNumber.toLowerCase().includes(q) ||
    p.name.toLowerCase().includes(q) ||
    p.alternatePartNumbers.some(ap => ap.toLowerCase().includes(q)) ||
    p.comparableBrands.some(cb => cb.toLowerCase().includes(q)) ||
    p.description.toLowerCase().includes(q)
  );
}
